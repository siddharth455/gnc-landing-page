  <script>
    AOS.init({ duration: 1000, once: true });

    // Navbar background on scroll
    window.addEventListener('scroll', function () {
      const nav = document.querySelector('.navbar');
      if (window.scrollY > 100) nav.classList.add('scrolled');
      else nav.classList.remove('scrolled');
    });

    // Filter logic
    const filterBtns = document.querySelectorAll('.filter-btn');
    const filterItems = document.querySelectorAll('.filter-item');
    filterBtns.forEach(btn => {
      btn.addEventListener('click', () => {
        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        const filter = btn.getAttribute('data-filter');
        filterItems.forEach(item => {
          if (filter === 'all' || item.classList.contains(filter)) {
            item.style.display = 'block';
          } else {
            item.style.display = 'none';
          }
        });
      });
    });

    // Fill modal with course details
    const courseModal = document.getElementById('courseModal');
    courseModal.addEventListener('show.bs.modal', function (e) {
      const btn = e.relatedTarget;
      const title = btn.getAttribute('data-title');
      const duration = btn.getAttribute('data-duration');
      const fee = btn.getAttribute('data-fee');
      const desc = btn.getAttribute('data-description');
      courseModal.querySelector('.modal-title').textContent = title;
      document.getElementById('modal-duration').textContent = duration;
      document.getElementById('modal-fee').textContent = fee;
      document.getElementById('modal-description').textContent = desc;
    });

    // Animate stats counters
    function animateValue(id, start, end, duration) {
      let obj = document.getElementById(id);
      let range = end - start;
      let minTimer = 50;
      let stepTime = Math.abs(Math.floor(duration / range));
      stepTime = Math.max(stepTime, minTimer);
      let startTime = new Date().getTime();
      let endTime = startTime + duration;
      let timer;

      function run() {
        let now = new Date().getTime();
        let remaining = Math.max((endTime - now) / duration, 0);
        let value = Math.round(end - (remaining * range));
        obj.textContent = value + (obj.id === 'stat-package' ? ' LPA' : (obj.id === 'stat-alumni' ? '+' : (obj.id === 'stat-rate' ? '%' : '')));
        if (value === end) clearInterval(timer);
      }
      timer = setInterval(run, stepTime);
      run();
    }

    // On scroll, trigger stat animation once
    let statsAnimated = false;
    window.addEventListener('scroll', () => {
      const statSection = document.getElementById('placement');
      if (!statsAnimated && statSection.getBoundingClientRect().top < window.innerHeight) {
        animateValue('stat-companies', 0, 150, 2000);
        animateValue('stat-rate', 0, 98, 2000);
        animateValue('stat-package', 0, 10, 2000); // 10 LPA
        animateValue('stat-alumni', 0, 500, 2000);
        statsAnimated = true;
      }
    });
  </script>
</body>
</html>
